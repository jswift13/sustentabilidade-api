-- SETORES
CREATE TABLE SETORES (
  ID_SETOR             NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  NOME_SETOR           VARCHAR2(100) NOT NULL,
  AREA_M2              NUMBER(10,2),
  META_CONSUMO_KWH     NUMBER(10,2),
  RESPONSAVEL          VARCHAR2(100),
  ATIVO                NUMBER(1) DEFAULT 1,
  CREATED_AT           TIMESTAMP DEFAULT SYSTIMESTAMP
);

-- PAINEIS_SOLARES (pai de GERACAO_SOLAR)
CREATE TABLE PAINEIS_SOLARES (
  ID_PAINEL            NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  CODIGO_PAINEL        VARCHAR2(50) UNIQUE NOT NULL,
  LOCALIZACAO          VARCHAR2(100),
  CAPACIDADE_KWP       NUMBER(10,2) NOT NULL,
  FABRICANTE           VARCHAR2(100),
  DATA_INSTALACAO      DATE NOT NULL,
  STATUS               VARCHAR2(20) DEFAULT 'OPERACIONAL',
  CREATED_AT           TIMESTAMP DEFAULT SYSTIMESTAMP
);

-- EQUIPAMENTOS (depende de SETORES)
CREATE TABLE EQUIPAMENTOS (
  ID_EQUIPAMENTO       NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  ID_SETOR             NUMBER NOT NULL,
  NOME_EQUIPAMENTO     VARCHAR2(100) NOT NULL,
  TIPO_EQUIPAMENTO     VARCHAR2(50),
  POTENCIA_WATTS       NUMBER(10,2) NOT NULL,
  STATUS               VARCHAR2(20) DEFAULT 'ATIVO',
  DATA_INSTALACAO      DATE,
  CREATED_AT           TIMESTAMP DEFAULT SYSTIMESTAMP,
  CONSTRAINT FK_EQUIP_SETOR FOREIGN KEY (ID_SETOR) REFERENCES SETORES(ID_SETOR)
);

-- CONSUMO_ENERGIA (depende de EQUIPAMENTOS)
CREATE TABLE CONSUMO_ENERGIA (
  ID_CONSUMO           NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  ID_EQUIPAMENTO       NUMBER NOT NULL,
  DATA_HORA_LEITURA    TIMESTAMP NOT NULL,
  CONSUMO_KWH          NUMBER(10,4) NOT NULL,
  CUSTO_ESTIMADO       NUMBER(10,2),
  PERIODO_HORAS        NUMBER(5,2),
  ALERTA_EXCESSO       NUMBER(1) DEFAULT 0,
  CREATED_AT           TIMESTAMP DEFAULT SYSTIMESTAMP,
  CONSTRAINT FK_CONSUMO_EQUIP FOREIGN KEY (ID_EQUIPAMENTO) REFERENCES EQUIPAMENTOS(ID_EQUIPAMENTO)
);

-- GERACAO_SOLAR (depende de PAINEIS_SOLARES)
CREATE TABLE GERACAO_SOLAR (
  ID_GERACAO           NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  ID_PAINEL            NUMBER NOT NULL,
  DATA_HORA_LEITURA    TIMESTAMP NOT NULL,
  ENERGIA_GERADA_KWH   NUMBER(10,4) NOT NULL,
  IRRADIACAO_SOLAR     NUMBER(10,2),
  TEMPERATURA_PAINEL   NUMBER(5,2),
  EFICIENCIA_PERCENTUAL NUMBER(5,2),
  ECONOMIA_CO2_KG      NUMBER(10,2),
  CREATED_AT           TIMESTAMP DEFAULT SYSTIMESTAMP,
  CONSTRAINT FK_GERACAO_PAINEL FOREIGN KEY (ID_PAINEL) REFERENCES PAINEIS_SOLARES(ID_PAINEL)
);

